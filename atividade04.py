# Leia dois números e mostre:
# A soma
# A subtração
# A multiplicação
# A divisão

numero1 = int(input("digite seu primeiro número: "))
numero2 = int(input("digite seu segundo número: "))
soma = numero1 + numero2 
print(f'a soma é: {soma}')
subtração = numero1 - numero2 
print(f'a subtração é: {subtração}')
multiplicação = numero1 * numero2
print(f'a multiplicação é: {multiplicação}')
divisão = numero1/numero2
print(f'a divisão é: {divisão}')